<script lang="ts">
import { Component, Vue } from "vue-property-decorator";

import axios from 'axios';
import { getUser } from '../utils/endpoints';

import { _auth } from '@/utils/index';

@Component
export default class Overview extends Vue {
    user_id: string = localStorage.getItem("user_id")!;
    user: {} = {};

  mounted() {
    _auth();
    this.fetchUserDetails(this.user_id);

  }

  fetchUserDetails(user_id: string) {
    axios.get(`${getUser}/${user_id}`)
      .then(res => this.user = res.data[0])
      .catch(err => console.log("Error fetching user details :>>", err));
  }

  logout() {
    localStorage.removeItem("user_id");
    this.$router.replace("/");
  }
}

</script>